  Language(String locale) {
    this(locale, null);
  }

  public String locale() {
    return locale;
  }

  public String unicode() {
    return unicode;
  }

  public static Language unicode(String unicode) {
    for (Language language : values()) {
      if (language.unicode() != null
        && language.unicode().equals(unicode)) return language;
    }
    return null;
  }

  public static Language locale(String locale) {
    for (Language language : values()) {
      if (language.locale().equalsIgnoreCase(locale)) return language;
    }
    return null;
  }
